// 模拟 AI 回复（用于演示 UI，不连接真实模型）

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const normalize = (s = '') => String(s).trim().toLowerCase();

const template = ({ title, summary, advice = [], redFlags = [] }) => {
  const a = advice.length ? advice.map((x) => `- ${x}`).join('\n') : '-（暂无）';
  const r = redFlags.length ? redFlags.map((x) => `- ${x}`).join('\n') : '-（暂无）';
  return [
    `【结论】${title}`,
    `【简要说明】${summary}`,
    `【建议】\n${a}`,
    `【需要尽快就医/急诊的情况】\n${r}`,
    `【免责声明】此回答仅用于健康科普与护理知识学习，不能替代医生诊断与处方。如有紧急症状请立即就医。`
  ].join('\n\n');
};

